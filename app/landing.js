const DEFAULT_LANGUAGE = "en";
const languageSelect = document.querySelector("#landing-language");

function savedLanguage() {
  try {
    const saved = localStorage.getItem("freireai-landing-language");
    return content[saved] ? saved : null;
  } catch {
    return null;
  }
}

function browserLanguage() {
  const browserLanguages = [
    ...(Array.isArray(navigator.languages) ? navigator.languages : []),
    navigator.language,
    navigator.userLanguage
  ].filter(Boolean);

  const match = browserLanguages.find((language) => {
    const normalized = language.toLowerCase();
    return normalized.startsWith("pt") || normalized.startsWith("es") || normalized.startsWith("en");
  });

  if (!match) return null;
  if (match.toLowerCase().startsWith("pt")) return "pt-BR";
  if (match.toLowerCase().startsWith("es")) return "es";
  return "en";
}

function preferredLanguage() {
  return savedLanguage() || browserLanguage() || DEFAULT_LANGUAGE;
}

function setLanguage(language) {
  const nextLanguage = content[language] ? language : DEFAULT_LANGUAGE;
  const nextContent = content[nextLanguage];
  document.documentElement.lang = nextLanguage;
  document.title = nextContent.heroTitle;
  languageSelect.value = nextLanguage;
  try {
    localStorage.setItem("freireai-landing-language", nextLanguage);
  } catch {
    // Language detection still works when storage is unavailable.
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = nextContent[element.dataset.i18n];
  });

  renderVideos(nextLanguage);
  renderScreenshots(nextLanguage);
}

function renderVideos(language) {
  const selectedVideos = videos[language] || videos[DEFAULT_LANGUAGE];
  const pitchLink = document.querySelector("#pitch-video-link");
  const pitchThumb = document.querySelector("#pitch-video-thumb");
  const pitchMeta = document.querySelector("#pitch-video-meta");
  const projectLink = document.querySelector("#project-video-link");
  const projectThumb = document.querySelector("#project-video-thumb");
  const projectMeta = document.querySelector("#project-video-meta");

  pitchLink.href = selectedVideos.pitch.url;
  pitchThumb.src = selectedVideos.pitch.thumbnail;
  pitchThumb.alt = `${selectedVideos.pitch.title} video thumbnail - ${selectedVideos.label}`;
  pitchMeta.textContent = selectedVideos.label;

  projectLink.href = selectedVideos.project.url;
  projectThumb.src = selectedVideos.project.thumbnail;
  projectThumb.alt = `${selectedVideos.project.title} video thumbnail - ${selectedVideos.label}`;
  projectMeta.textContent = selectedVideos.label;
}

function renderScreenshots(language) {
  const selectedScreenshots = screenshots[language] || screenshots[DEFAULT_LANGUAGE];
  const selectedContent = content[language] || content[DEFAULT_LANGUAGE];
  const screenIds = [
    "screen-map-img",
    "screen-codify-img",
    "screen-question-img",
    "screen-listening-img",
    "screen-circle-img"
  ];

  document.documentElement.style.setProperty("--hero-image", `url("${selectedScreenshots.main}")`);

  const mainScreenshot = document.querySelector("#main-screenshot");
  mainScreenshot.src = selectedScreenshots.main;
  mainScreenshot.alt = selectedContent.screenshotAlt;

  screenIds.forEach((id, index) => {
    const image = document.querySelector(`#${id}`);
    image.src = selectedScreenshots.screens[index];
    image.alt = `${selectedContent.screenshotAlt} - ${index + 1}`;
  });
}

function adjustDemoLinksForLocalFiles() {
  if (window.location.protocol !== "file:") return;
  document.querySelectorAll(".demo-link").forEach((link) => {
    link.setAttribute("href", "index.html");
  });
}

languageSelect.addEventListener("change", (event) => setLanguage(event.target.value));
adjustDemoLinksForLocalFiles();
setLanguage(preferredLanguage());
